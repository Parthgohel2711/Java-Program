public class DataType 
{
    public static void main(String[] args)
    {
        Integer A = 10;
        Double C = 34.7;
        Character D = 'P';
        Boolean E = true;

        System.out.println("Integer : "+A);
        System.out.println("Double : "+C);
        System.out.println("Character : "+D);
        System.out.println("Boolean : "+E);

        int a = 5;
        double c = 45.8;
        char d = 'G';
        boolean e = false;

        System.out.println("integer : "+a);
        System.out.println("double : "+c);
        System.out.println("character : "+d);
        System.out.println("boolean : "+e);


    }
}